// addition
function add (a, b) {
let result = a + b;
return result;
}
// subtraction
function subtract(a, b){
    let result = a - b;
    return result;
}
// multiplication
function multiply(a,b){
    let result = a * b;
    return result;
}
// division
function divide(a,b){
    let result = a / b;
    return result;
}